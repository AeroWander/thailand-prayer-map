/**
 * Generates src/data/campuses.ts from the master institution list.
 * Run: node scripts/generate-campuses.mjs
 */
import { writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';
import { institutions } from './institutions-master.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const outPath = join(__dirname, '../src/data/campuses.ts');

const prayedIds = new Set([
  'chulalongkorn-university',
  'thammasat-university',
  'khon-kaen-university',
  'buriram-rajabhat-university',
  'silpakorn-university',
  'ubon-ratchathani-university',
  'naresuan-university',
]);

const campuses = institutions.map((inst) => ({
  id: inst.id,
  name: inst.name,
  nameTh: inst.nameTh,
  university: inst.name,
  province: inst.province,
  region: inst.region,
  type: inst.type,
  studentPopulation: inst.studentPopulation,
  foundingYear: inst.foundingYear,
  lat: inst.lat,
  lng: inst.lng,
  website: inst.website,
  prayedFor: prayedIds.has(inst.id),
}));

const content = `// Auto-generated by scripts/generate-campuses.mjs — do not edit by hand
import type { Campus } from '../types/campus';

export const campuses: Campus[] = ${JSON.stringify(campuses, null, 2)};
`;

writeFileSync(outPath, content, 'utf8');
console.log(`Generated ${campuses.length} campuses -> ${outPath}`);
